namespace osgVerse
{
    std::map<std::string, std::pair<std::string, GLenum>> createGLEnumMapper()
    {
        static std::map<std::string, std::pair<std::string, GLenum>> glEnums;
        for (int i = 0; ; i += 2)
        {
            std::string t = osgv_builtinGLEnumMappings[i]; if (t.length() == 0) break;
            std::string g = osgv_builtinGLEnumMappings[i + 1], key; unsigned int value = 0;
            std::stringstream ss(t); ss >> key >> std::hex >> value;
            glEnums[key] = std::pair<std::string, GLenum>(g, (GLenum)value);
        }
        return glEnums;
    }
}
